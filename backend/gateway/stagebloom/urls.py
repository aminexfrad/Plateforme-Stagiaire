from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

# Swagger schema view
schema_view = get_schema_view(
    openapi.Info(
        title="StageBloom API",
        default_version='v1',
        description="API pour la gestion des stages et demandes",
        terms_of_service="https://www.stagebloom.com/terms/",
        contact=openapi.Contact(email="contact@stagebloom.com"),
        license=openapi.License(name="MIT License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    
    # API endpoints
    path('api/auth/', include('auth_service.urls')),
    path('api/demandes/', include('demande_service.urls')),
    path('api/stagiaire/', include('stagiaire_service.urls')),
    path('api/admin/', include('admin_service.urls')),
    path('api/rh/', include('rh_service.urls')),
    path('api/tuteur/', include('tuteur_service.urls')),
    path('api/', include('shared.urls')),
    path('api/notifications/', include('notification_service.urls')),
    
    # Swagger documentation
    path('api/docs/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('api/redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
